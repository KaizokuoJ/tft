<template>
  <b-row>
    <b-col>
      <b-table
        v-if="selectedChampions.length !== 0"
        stacked="md"
        show-empty
        :empty-text="getTableEmptyText()"
        :items="teamCompositionsToRender"
        :fields="teamCompositionsToRenderFields"
        class="text-white"
        sort-by="tier"
      >
        <template slot="tier" slot-scope="row">
          <div class="tier-number mx-auto">
            {{ convertTierNumberToLetter(row.item.tier) }}
          </div>
        </template>
        <template slot="synergies" slot-scope="row">
          <div
            v-for="synergy in getSynergiesSorted(row.item.synergies)"
            class="d-inline-block ml-3"
          >
            <img
              :src="getClassOrOriginThumbnailImage(synergy.type)"
              alt=""
              class="class-or-origin-thumbnail-image d-block mx-auto"
            />
            <span class="font-weight-bold">
              {{ synergy.type }}
            </span>
          </div>
        </template>
        <template slot="size" slot-scope="row">
          <span class="font-weight-bold">{{
            row.item.championNames.length
          }}</span>
        </template>

        <template slot="champions" slot-scope="row">
          <mq-layout mq="lg+">
            <div
              v-for="champion in getChampionsSortedByCost(row.item.champions)"
              class="d-inline-block"
            >
              <div
                class="champion-thumbnail champion-thumbnail-image-container d-inline-block"
                :class="getChampionThumbnailContainerClass(champion)"
              >
                <img
                  :src="getChampionThumbnailImage(champion.key)"
                  alt="Image of a champion that is a part of a team composition generated by the user's input"
                  class="champion-thumbnail champion-thumbnail-image"
                />
              </div>
              <span class="d-block">{{ champion.name }}</span>
              <div>
                <img
                  v-for="item in champion.items"
                  :src="getItemThumbnailImage(item)"
                  alt=""
                  class="item-thumbnail-image my-1 d-block mx-auto"
                />
              </div>
            </div>
          </mq-layout>

          <mq-layout :mq="['sm', 'md']">
            <div
              v-for="champion in getChampionsSortedByCost(row.item.champions)"
              class="d-flex justify-content-center"
            >
              <div class="d-flex flex-column align-items-start mb-2">
                <div>{{ champion.name }}</div>
                <div class="item-hl d-flex align-items-center border-bottom">
                  <div
                    class="champion-thumbnail champion-thumbnail-image-container d-inline-block"
                    :class="getChampionThumbnailContainerClass(champion)"
                  >
                    <img
                      :src="getChampionThumbnailImage(champion.key)"
                      alt="Image of a champion that is a part of a team composition generated by the user's input"
                      class="champion-thumbnail champion-thumbnail-image"
                    />
                  </div>
                  <img
                    v-for="item in champion.items"
                    :src="getItemThumbnailImage(item)"
                    alt=""
                    class="item-thumbnail-image pl-2"
                  />
                </div>
              </div>
            </div>
          </mq-layout>
        </template>
      </b-table>
    </b-col>
  </b-row>
</template>

<script>
export default {
  data() {
    return {
      availableChampions: [],
      teamCompositionsToRenderFields: [
        { key: "tier", sortable: true },
        { key: "championNames.length", label: "Team Size", sortable: true },
        { key: "synergies" },
        { key: "champions" }
      ]
    };
  },
  props: ["teamCompositionsToRender", "selectedChampions"],
  methods: {
    getChampionThumbnailImage(championKey) {
      const formattedChampionName = this.formatChampionName(championKey);
      return require(`@/assets/images/championImages/${this.capitalizeFirstLetter(
        formattedChampionName
      )}.png`);
    },
    getClassOrOriginThumbnailImage(classOrOriginName) {
      return require(`@/assets/images/classAndOriginImages/${this.capitalizeFirstLetter(
        classOrOriginName
      )}.png`);
    },
    getItemThumbnailImage(itemName) {
      return require(`@/assets/images/items/${itemName}.png`);
    },
    getChampionThumbnailContainerClass(champion) {
      return {
        "champion-thumbnail-image-container-5-cost": champion.cost === 5,
        "champion-thumbnail-image-container-4-cost": champion.cost === 4,
        "champion-thumbnail-image-container-3-cost": champion.cost === 3,
        "champion-thumbnail-image-container-2-cost": champion.cost === 2,
        "champion-thumbnail-image-container-1-cost": champion.cost === 1
      };
    },
    getTableEmptyText() {
      if (this.selectedChampions.length === 0) {
        return "No champion entered yet";
      } else if (this.selectedChampions.length === 1) {
        return "Sorry, currently there are no meta compositions that include your given champion";
      } else {
        return "Sorry, currently there are no meta compositions that include your given champions";
      }
    },
    capitalizeFirstLetter: string => {
      return string.charAt(0).toUpperCase() + string.slice(1);
    },
    getSynergiesSorted: function(synergies) {
      return synergies.slice().sort(this.compareSynergyTypes);
    },
    getChampionsSortedByCost: function(champions) {
      return champions.slice().sort(this.compareChampionCost);
    },
    compareChampionCost: function(a, b) {
      if (a.cost < b.cost) {
        return -1;
      }
      if (a.cost > b.cost) {
        return 1;
      }
      return 0;
    },
    compareSynergyTypes: function(a, b) {
      if (a.type < b.type) {
        return -1;
      }
      if (a.type > b.type) {
        return 1;
      }
      return 0;
    },

    convertTierNumberToLetter: function(tierNumber) {
      if (tierNumber === 1) {
        return "S";
      } else if (tierNumber === 2) {
        return "A";
      } else if (tierNumber === 3) {
        return "B";
      } else if (tierNumber === 4) {
        return "C";
      }
    },

    formatChampionName(championName) {
      if (championName === "AurelionSol") {
        return "Aurelion Sol";
      } else if (championName === "MissFortune") {
        return "Miss Fortune";
      } else if (championName === "Chogath") {
        return "Cho'gath";
      } else if (championName === "RekSai") {
        return "Rek'sai";
      } else if (championName === "Khazix") {
        return "Kha'zix";
      } else {
        return championName;
      }
    }
  }
};
</script>

